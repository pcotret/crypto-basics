/* RotLine
 * Input. msg: a 4 bytes vector
 * Output. msg: same vector "shifted" to the left
 * Example. a0,a1,a2,a3 => a1,a2,a3,a0
 */
void RotLine(uint8_t msg[4])
{
    int j;
    uint8_t temp;
    temp=msg[0];
    for(j=0;j<3;j++)
    {
        msg[j]=msg[j+1];
    }
    msg[3]=temp;
}
/* KeyExpansion
 * Input. cipher_key: the cipher key
 * Output. round_key: matrix containing all round keys
 */
void KeyExpansion(uint8_t cipher_key[4*Nk],uint8_t round_key[Nr+1][4*Nb])
{
    int i,k,l;
    uint8_t temp_line[4]={0x00,0x00,0x00,0x00};
    uint8_t x_index,y_index;
    for(i=0;i<4*Nb;i++)
    {
        round_key[0][i]=cipher_key[i];
    }
    for(l=0;l<Nr;l++)
    {
        for(i=0;i<4;i++)
        {
            temp_line[i]=round_key[l][3*Nb+i];
        }
        RotLine(temp_line);
        for(i=0;i<4;i++)
        {
            x_index=temp_line[i]/0x10;
            y_index=temp_line[i]-(x_index*0x10);
            temp_line[i]=SBOX[x_index][y_index];
        }
        for(i=0;i<4;i++)
        {
            temp_line[i]=temp_line[i]^ROT_CON[Nr*i+l];
        }
        for(i=0;i<4;i++)
        {
            round_key[l+1][i]=round_key[l][i]^temp_line[i];
        }
        for(k=0;k<3;k++)
        {
            for(i=0;i<4;i++)
            {
                temp_line[i]=round_key[l][(k+1)*Nb+i];
            }
            for(i=0;i<4;i++)
            {
                round_key[l+1][(k+1)*Nb+i]=round_key[l+1][k*Nb+i]^temp_line[i];
            }
        }
    }
}
/* AddRoundKey
 * Input. input_msg/key: message and key
 * Output. input_msg: the "XOR-ed" version
 */
void AddRoundKey(uint8_t input_msg[4*Nb],uint8_t key[4*Nb])
{
    int i;
    for(i=0;i<4*Nb;i++)
    {
        input_msg[i]=input_msg[i]^key[i];
    }
}
/* SubBytes
 * Input. input_msg: message
 * Output. input_msg: processed through SBOX
 */
int SubBytes(uint8_t input_msg[4*Nb])
{
    int i;
    uint8_t x_index,y_index;
    for(i=0;i<4*Nb;i++)
    {
        x_index=input_msg[i]/0x10;
        y_index=input_msg[i]-(x_index*0x10);
        input_msg[i]=SBOX[x_index][y_index];
    }
    return 0;
}
/* RotMatrix
 * Input. lineNumber/msg.
 * Output. msg: w/ permutations similar to RotLine
 */
void RotMatrix(int lineNumber,uint8_t msg[4*Nb])
{
    int i,j;
    uint8_t temp;
    for(i=0;i<lineNumber;i++)
    {
        temp=msg[lineNumber];
        for(j=0;j<Nb-1;j++)
        {
            msg[lineNumber+j*Nb]=msg[lineNumber+(j+1)*Nb];
        }
        msg[lineNumber+(Nb-1)*Nb]=temp;
    }
}
/* ShiftRows
 * Line permutations w/ different shift values
 */
void ShiftRows(uint8_t input_msg[4*Nb])
{
    RotMatrix(1,input_msg);
    RotMatrix(2,input_msg);
    RotMatrix(3,input_msg);
}
/* gmult: taken from https://en.wikipedia.org/wiki/Finite_field_arithmetic
 * Input. a/b
 * Output. result: equals a*b in GF(2^8)
 */
uint8_t gmult(uint8_t a, uint8_t b)
{
	uint8_t result=0;
	while(b)
    {
        if(b&1)
        {
            result^=a;
        }
        if(a&0x80)
        {
            a=(a<<1)^0x11b;
        }
        else
        {
            a<<=1;
        }
        b>>=1;
	}
	return result;
}
/* MixColumns
 * Input. input_msg: message
 * Output. input_msg: message processed w/ MixColumns
 */
void MixColumns(uint8_t input_msg[4*Nb])
{
    int j;
    uint8_t temp_msg[4*Nb]={0x00,0x00,0x00,0x00,
                            0x00,0x00,0x00,0x00,
                            0x00,0x00,0x00,0x00,
                            0x00,0x00,0x00,0x00};
    for(j=0;j<4;j++)
    {
        temp_msg[j*Nb+0]^=gmult(input_msg[j*Nb+0],0x02)^gmult(input_msg[j*Nb+1],0x03)^gmult(input_msg[j*Nb+2],0x01)^gmult(input_msg[j*Nb+3],0x01);
        temp_msg[j*Nb+1]^=gmult(input_msg[j*Nb+0],0x01)^gmult(input_msg[j*Nb+1],0x02)^gmult(input_msg[j*Nb+2],0x03)^gmult(input_msg[j*Nb+3],0x01);
        temp_msg[j*Nb+2]^=gmult(input_msg[j*Nb+0],0x01)^gmult(input_msg[j*Nb+1],0x01)^gmult(input_msg[j*Nb+2],0x02)^gmult(input_msg[j*Nb+3],0x03);
        temp_msg[j*Nb+3]^=gmult(input_msg[j*Nb+0],0x03)^gmult(input_msg[j*Nb+1],0x01)^gmult(input_msg[j*Nb+2],0x01)^gmult(input_msg[j*Nb+3],0x02);
    }
    for(j=0;j<4*Nb;j++)
    {
        input_msg[j]=temp_msg[j];
    }
}
/* Cipher: the lop level function
 */
void Cipher(uint8_t input_msg[4*Nb],uint8_t cipher_key[4*Nb])
{
    int i,k;
    KeyExpansion(cipher_key,round_key);
    AddRoundKey(input_msg,cipher_key);
    for(i=1;i<Nr;i++)
    {
        SubBytes(input_msg);
        ShiftRows(input_msg);
        MixColumns(input_msg);
        for(k=0;k<4*Nb;k++)
        {
            cipher_key[k]=round_key[i][k];
        }
        AddRoundKey(input_msg,cipher_key);
    }
    SubBytes(input_msg);
    ShiftRows(input_msg);
    for(k=0;k<4*Nb;k++)
    {
        cipher_key[k]=round_key[10][k];
    }
    AddRoundKey(input_msg,cipher_key);
}